    // Updates the GUI cells according to the current multi-linked list state

    public void updateGrid() {
        for (int row = 0; row < 7; row++) {
            for (int col = 0; col < 5; col++) {

                Node node = grid.findNode(row, col);

                cells[row][col].setOpaque(true);

                if (node != null) {
                    cells[row][col].setText(String.valueOf(node.value));
                    cells[row][col].setBackground(getTileColor(node.value));
                } else {
                    cells[row][col].setText(".");
                    cells[row][col].setBackground(java.awt.Color.WHITE);
                }
            }
        }
    }

