    private void nextbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextbuttonActionPerformed
        if (isMergeAnimationRunning) {
            return;
        }

        if (step < moves.length) {
 /*
             * Get the current move.
             * moves[step][0] is the tile value.
             * moves[step][1] is the target column.
             */
            int tileValue = moves[step][0];
            int targetColumn = moves[step][1];
            infoLabel.setText("STEP " + (step + 1) + " - Tile " + tileValue
                    + " will be dropped into column " + targetColumn + ".");
       
            if (grid.getColumnSize(targetColumn) == 7) {
                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "Game Over! Column " + targetColumn + " is full.",
                        "Drop Number Game",
                        javax.swing.JOptionPane.WARNING_MESSAGE
                );
                nextbutton.setEnabled(false);
                return;
            }
            // Add the tile first, then show merge operations step by step
            grid.addTileOnly(targetColumn, tileValue);

            step++;
            stepLabel.setText("STEP : " + step);

            updateGrid();

            // Start merge animation for the selected column
            isMergeAnimationRunning = true;
            columnBeingMerged = targetColumn;

            nextbutton.setEnabled(false);

            mergeAnimationTimer = new javax.swing.Timer(800, e -> {

                boolean didMergeHappen = grid.mergeOneStep(columnBeingMerged);

                updateGrid();
                //If no merge is left, stop animation and enable the button again
                
                if (!didMergeHappen) {
                    mergeAnimationTimer.stop();
                    isMergeAnimationRunning = false;
                    nextbutton.setEnabled(true);

                   // If all scenario steps are finished, show game over message
                    if (step >= moves.length) {
                        javax.swing.JOptionPane.showMessageDialog(
                                this,
                                "Game Over! Scenario finished.",
                                "Drop Number Game",
                                javax.swing.JOptionPane.INFORMATION_MESSAGE
                        );
                        nextbutton.setEnabled(false);
                    }
                }
            });

            mergeAnimationTimer.setInitialDelay(1200);
            mergeAnimationTimer.start();
        }
    }
    
        // Resets the game board and restarts the scenario from the beginning
    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        
        // Stop merge animation if it is currently running
        if (mergeAnimationTimer != null && mergeAnimationTimer.isRunning()) {
            mergeAnimationTimer.stop();
        }
        // Clear animation state
        isMergeAnimationRunning = false;
        columnBeingMerged = -1;
        
        // Create a new empty multi-linked list and reset step counter
        grid = new MultiLinkedList();
        step = 0;

        // Reset GUI labels
        stepLabel.setText("STEP : 0");
        infoLabel.setText("Scenario information will appear here.");

        nextbutton.setEnabled(true);

         // Refresh the board visually
        updateGrid();
    }//GEN-LAST:event_resetButtonActionPerformed

